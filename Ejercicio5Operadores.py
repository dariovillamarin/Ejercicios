# OPERADORES ARITMETICOS

x=10
y=5

# SUMA
print("La Suma de X + Y es: ",x+y)

# RESTA
print("La resta de X - Y es: ", x-y)

# MULTIPLICACION
print("La Multipliación de X * Y es: ", x*y)

# DIVISION
print("La división de X / Y es: ", x/y)

# PRODUCTO ENTERO DE UNA DIVISION
print("El producto entero de la división X // Y es: ", x//y)

# EXPONENTE
print ("El exponente de X ** Y es: ", x**5)


# OPERADORES COMPARATIVOS

# MAYOR QUE
print("X es mayor que Y ", x>y)

# MENOR QUE
print("X es menor que Y ", x<y)

# IGUAL A
print("X es igual a Y ", x==y)

# NO ES IGUAL A
print("X no es igual a Y ", x!=y)

# MAYOR O IGUAL
print("X es mayor o igual a Y ", x>=y)

# MENOR O IGUAL
print("X es menor o igual a Y", x<=y)